<template>
  <div
    v-if="isExternal"
    :style="styleExternalIcon"
    class="svg-external-icon svg-icon"
    v-on="$attrs"
  />
  <svg v-else :class="svgClass" arig-hidden="true" v-on="$attrs">
    <use :href="iconName" />
  </svg>
</template>

<script>
import * as utils from '@/utils'

export default {
  name: 'SvgIcon',
  props: {
    iconClass: {
      type: String,
      required: true
    },
    className: {
      type: String,
      default: ''
    }
  },
  computed:{
    isExternal(){
      return utils.isExternal(this.$props.iconClass)
    },
    iconName(){
      return `#icon-${this.$props.iconClass}`
    },
    svgClass() {
      if (this.$props.className) {
        return `svg-icon ${this.$props.className}`
      } else {
        return 'svg-icon'
      }
    },
    styleExternalIcon() {
      return {
        mask: `url(${this.$props.iconClass}) no-repeat 50% 50%`,
      '-webkit-mask': `url(${this.$props.iconClass}) no-repeat 50% 50%`
      }
    },
  },
}
</script>

<style scoped>
.svg-icon{
  width : 1em;
  height : 1em;
  vertical-align : -0.15em;
  fill : currentColor;
  overflow : hidden;
}
  
.svg-external-icon{
  background-color : currentColor;
  mask-size : cover !important;
  display : inline-block;
}
  
</style>
